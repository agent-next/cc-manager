class FileCreatingDispatcher:
    """模拟 CC Dispatcher：在 worktree 中创建文件并 commit。"""

    def __init__(self, delay=0.01):
        self.delay = delay
        self.system_prompt = ""

    async def run(self, task, workdir=None, on_event=None):
        await asyncio.sleep(self.delay)
        task.started_at = "2026-01-01T00:00:00"

        try:
            # 模拟 CC agent 的工作：创建文件
            filename = f"output_{task.id}.py"
            filepath = Path(workdir) / filename
            filepath.write_text(f"# Generated by task {task.id}\nresult = 'OK'\n")

            # Git add + commit
            subprocess.run(["git", "add", "."], cwd=workdir,
                           capture_output=True, check=True)
            subprocess.run(
                ["git", "commit", "-m", f"feat: complete task {task.id}"],
                cwd=workdir, capture_output=True, check=True,
                env={**os.environ,
                     "GIT_AUTHOR_NAME": "agent", "GIT_AUTHOR_EMAIL": "a@t.com",
                     "GIT_COMMITTER_NAME": "agent", "GIT_COMMITTER_EMAIL": "a@t.com"}
            )

            task.status = TaskStatus.SUCCESS
            task.output = f"Created {filename}"
            task.cost_usd = 0.01
        except Exception as e:
            task.status = TaskStatus.FAILED
            task.error = str(e)

        task.completed_at = "2026-01-01T00:00:02"

        if on_event:
            await on_event({"type": "result", "task_id": task.id, "status": task.status.value})

        return task


